# ЗАДАЧА 1
import random
random.seed(42)
A = [random.randint(-20, 20) for _ in range(15)]
print("Последовательность:", A)

positive = [x for x in A if x > 0]
print("Положительные:", positive)
print("Количество:", len(positive))

