# Данные магазинов
magnit = {'молоко', 'соль', 'сахар', 'печенье', 'сыр'}
pyaterochka = {'мясо', 'молоко', 'сыр'}
perekrestok = {'молоко', 'творог', 'сыр', 'сахар', 'печенье'}
lenta = {'печенье', 'молоко', 'сыр', 'сметана'}

print("Магнит:", magnit)
print("Пятерочка:", pyaterochka)
print("Перекресток:", perekrestok)
print("Лента:", lenta)
print("-" * 40)

# 1. В каких магазинах нельзя приобрести сметану
print("1. Сметану нельзя купить в:", end=" ")
if 'сметана' not in magnit: print("Магнит", end=" ")
if 'сметана' not in pyaterochka: print("Пятерочка", end=" ")
if 'сметана' not in perekrestok: print("Перекресток", end=" ")
if 'сметана' not in lenta: print("Лента", end=" ")
print("\n")

# 2. Товары из Магнита, отсутствующие в Перекрестке
print("2. Товары Магнита, которых нет в Перекрестке:", magnit - perekrestok)

# 3. Товары из Ленты, отсутствующие в Магните
print("3. Товары Ленты, которых нет в Магните:", lenta - magnit)
